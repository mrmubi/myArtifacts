using System;

namespace myUtilities
{
    public class Math
    {

        public int mySum(int numberA, int numberB) {
            return numberA + numberB;
        }

        public int myMultiply(int numberA, int numberB)
        {
            return numberA * numberB;
        }

        public int myDivide(int numberA, int numberB)
        {
            return numberA / numberB;
        }

       


    }
}
